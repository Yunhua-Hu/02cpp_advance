#pragma once  // 防止头文件重复包含
#include <iostream>
using namespace std;

template<class T1, class T2>
class Person7 {
public:
	Person7(T1 name, T2 age);

	void showPerson7();

	T1 m_Name;
	T2 m_Age;
};

// 构造函数类外实现
template<class T1, class T2>
Person7<T1, T2>::Person7(T1 name, T2 age) {
	this->m_Name = name;
	this->m_Age = age;
}

// 成员函数类外实现
template<class T1, class T2>
void Person7<T1, T2>::showPerson7() {
	cout << "姓名: " << this->m_Name << " 年龄: " << this->m_Age << endl;

}
